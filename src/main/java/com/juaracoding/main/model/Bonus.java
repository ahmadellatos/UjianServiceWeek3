package com.juaracoding.main.model;

public class Bonus {
	private int working_ref_id;
	private String bonus_date;
	private int bonus_amount;
	
	public Bonus() {
		
	}

	public int getWorking_ref_id() {
		return working_ref_id;
	}

	public void setWorking_ref_id(int working_ref_id) {
		this.working_ref_id = working_ref_id;
	}

	public String getBonus_date() {
		return bonus_date;
	}

	public void setBonus_date(String bonus_date) {
		this.bonus_date = bonus_date;
	}

	public int getBonus_amount() {
		return bonus_amount;
	}

	public void setBonus_amount(int bonus_amount) {
		this.bonus_amount = bonus_amount;
	}
	
	
}
